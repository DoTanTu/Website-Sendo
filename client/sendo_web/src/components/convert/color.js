function getColor(id){
    switch(id){
        case 1 :
            return "white";
        case 2 : 
            return "black";
        case 3 : 
            return "yellow";
        case 4 :
            return "red-500";
        case 5 :
            return "gray-600"
        case 6 :
            return "blue-500";
        case 7 :
            return "green-500";
    }
}
export default getColor;